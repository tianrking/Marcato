type Translation = typeof en;
type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends Record<string, unknown> ? DeepPartial<T[K]> : T[K];
};

function mergeTranslation(overrides: DeepPartial<Translation>): Translation {
  return {
    ...en,
    ...overrides,
    app: { ...en.app, ...overrides.app },
    action: { ...en.action, ...overrides.action },
    export: { ...en.export, ...overrides.export },
    view: { ...en.view, ...overrides.view },
    setting: { ...en.setting, ...overrides.setting },
    find: { ...en.find, ...overrides.find },
    status: { ...en.status, ...overrides.status },
    github: { ...en.github, ...overrides.github },
    toast: { ...en.toast, ...overrides.toast },
    error: { ...en.error, ...overrides.error },
    confirm: { ...en.confirm, ...overrides.confirm },
    health: {
      ...en.health,
      ...overrides.health,
      issue: { ...en.health.issue, ...overrides.health?.issue },
    },
  };
}

